// Create the light theme
const createLightTheme = () => {
  const lightTheme = createTheme({
    palette: palette.light,
    typography,
    shadows,
    shape,
    transitions,
    spacing: spacingUnit,
    breakpoints,
    components: {
      ...components,
      // Light theme specific overrides can go here
      MuiAppBar: {
        styleOverrides: {
          root: {
            boxShadow: shadows[2],
          },
        },
      },
    },
  });

  // Apply responsive typography settings
  return responsiveFontSizes(
    createTheme({
      ...lightTheme,
      typography: responsiveTypography(lightTheme),
    })
  );
};

// Create the dark theme
const createDarkTheme = () => {
  const darkTheme = createTheme({
    palette: palette.dark,
    typography,
    shadows,
    shape,
    transitions,
    spacing: spacingUnit,
    breakpoints,
    components: {
      ...components,
      // Dark theme specific overrides
      MuiAppBar: {
        styleOverrides: {
          root: {
            boxShadow: 'none',
            backgroundImage: 'none',
          },
        },
      },
      MuiPaper: {
        styleOverrides: {
          root: {
            backgroundImage: 'none',
          },
        },
      },
    },
  });

  // Apply responsive typography settings
  return responsiveFontSizes(
    createTheme({
      ...darkTheme,
      typography: responsiveTypography(darkTheme),
    })
  );
};

// Export themes
export const lightTheme = createLightTheme();
export const darkTheme = createDarkTheme();

// Export theme utilities
export const getTheme = (mode) => (mode === 'dark' ? darkTheme : lightTheme);
